package calculator;

public class Calc {
    public static int addNums(int a, int b){
        return a+b;
    }
    public static int substNums(int a, int b){
        return a-b;
    }
    public static int multNums(int a, int b){
        return a*b;
    }
    public static int divNums(int a, int b){
        return a/b;
    }
}
